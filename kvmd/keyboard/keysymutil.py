def xkb_utf32_to_keysym(ch: int) -> int:
    #  first check for Latin-1 characters (1:1 mapping) 
    if (0x0020 <= ch <= 0x007e) or (0x00a0 <= ch <= 0x00ff):
        return ch

    #  special keysyms 
    if (((0xff08 & 0x7f) <= ch <= (0xff0b & 0x7f)) or # XKB_KEY_BackSpace <= ch <= XKB_KEY_Clear
        ch == (0xff0d & 0x7f) or # ch == XKB_KEY_Return
        ch == (0xff1b & 0x7f)): # ch == XKB_KEY_Escape
        return ch | 0xff00
    if (ch == (0xffff & 0x7f)):
        return 0xffff

    #  Unicode non-symbols and code points outside Unicode planes
    if ((0xd800 <= ch <= 0xdfff) or 
        (0xfdd0 <= ch <= 0xfdef) or 
        ch > 0x10ffff or 
        (ch & 0xfffe) == 0xfffe):
        return 0x00000000

    #  search main table or use direct encoding if everything else fails 
    return _keysymtab[ch] if ch in _keysymtab else ch | 0x01000000
